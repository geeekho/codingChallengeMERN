const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const userValidation = require('../../validations/employee.validation');
const userController = require('../../controllers/employee.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageEmployees'), validate(userValidation.createEmployee), userController.createEmployee)
  .get(auth('getEmployees'), validate(userValidation.getEmployees), userController.getEmployees);

router
  .route('/:employeeId')
  .get(auth('getEmployees'), validate(userValidation.getEmployee), userController.getEmployee)
  .patch(auth('manageEmployees'), validate(userValidation.updateEmployee), userController.updateEmployee)
  .delete(auth('manageEmployees'), validate(userValidation.deleteEmployee), userController.deleteEmployee);

module.exports = router;

